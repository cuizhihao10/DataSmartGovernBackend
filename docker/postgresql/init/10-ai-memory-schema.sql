    CONSTRAINT uk_agent_memory_store_candidate UNIQUE (source_candidate_id),
    CONSTRAINT ck_agent_memory_store_memory_type CHECK (
        memory_type IN ('semantic', 'episodic', 'procedural', 'resource', 'short_term')
    ),
    CONSTRAINT ck_agent_memory_store_scope CHECK (
        scope IN ('session', 'project', 'tenant', 'global')
    ),
    CONSTRAINT ck_agent_memory_store_importance CHECK (
        importance_score >= 0 AND importance_score <= 1
    )
);

COMMENT ON TABLE agent_memory_store_entry IS
    '正式长期记忆低敏摘要表。它是模型可召回记忆的结构化事实源，pgvector、全文索引、图谱或对象存储都围绕 memory_id 扩展。';
COMMENT ON COLUMN agent_memory_store_entry.content IS
    '正式记忆低敏摘要。该字段可进入模型上下文，因此必须经过脱敏、保留期和 namespace 过滤。';
COMMENT ON COLUMN agent_memory_store_entry.content_search_vector IS
    'PostgreSQL 全文检索向量，用于没有向量或向量降级时的关键词召回。中文分词后续可替换为更专业扩展。';
COMMENT ON COLUMN agent_memory_store_entry.expires_at IS
    '记忆过期时间。所有检索查询必须排除过期记录，遗忘任务也应以该字段为清理依据。';

