    '低敏图状态快照。只能保存恢复执行所需的状态、指针和摘要，禁止保存完整 prompt、工具原始输出、密钥或大样本数据。';
COMMENT ON COLUMN langgraph_thread_checkpoint.next_nodes_json IS
    '下一批候选节点。边逻辑可根据该字段恢复分支、循环、人工介入或工具等待状态。';
COMMENT ON COLUMN langgraph_thread_checkpoint.resume_requirements_json IS
    '恢复执行前必须满足的事实条件，例如审批、澄清、预算、outbox ack 或 worker receipt。';

