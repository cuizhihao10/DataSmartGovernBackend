-- DataSmart Govern Backend - ai_memory PostgreSQL / pgvector V1 基线。
--
-- 本脚本服务于 Python AI Runtime、LangGraph 多智能体执行、长期记忆和用户画像能力。
-- 它刻意放在 PostgreSQL 初始化目录中，而不是继续放在 docker/mysql/migrations：
-- 1. PostgreSQL/pgvector 已经被确定为项目目标存储，MySQL 只保留迁移期兼容；
-- 2. ai_memory 与 agent_runtime 分 schema 隔离，避免把“Agent 控制面事实”和“模型可召回记忆正文/向量”混在一起；
-- 3. Python Runtime 当前没有 Spring/Flyway 运行时，因此新环境先通过 Compose init 脚本获得可运行基线；
-- 4. 已有 PostgreSQL 数据卷不会自动重跑 init 脚本，存量环境应由运维手工执行本文件或后续专用迁移工具。
--
-- 重要边界：
-- - 本脚本只保存低敏摘要、治理元数据、向量索引和 LangGraph durable state；
-- - 禁止把 prompt、原始 SQL、样本数据、完整工具输出、完整异常堆栈或密钥写入这些表；
-- - 真实客户环境应为 ai_memory 分配独立数据库角色、备份策略、保留期策略和审计权限。

