COMMENT ON TABLE langgraph_thread_checkpoint IS
    'LangGraph durable checkpoint 表。它让 Agent 图执行具备可恢复、可观测、可暂停和可分支的状态机能力，而不是只在进程内保存固定流水线状态。';
