SET search_path TO datasource_management, public;

CREATE TABLE IF NOT EXISTS datasource_schema_repair_plan (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    plan_ref VARCHAR(64) NOT NULL,
    tenant_id BIGINT NOT NULL,
    project_id BIGINT NOT NULL,
    datasource_id BIGINT NOT NULL,
    datasource_type VARCHAR(32) NOT NULL,
    operation VARCHAR(48) NOT NULL,
    schema_name VARCHAR(128),
    table_name VARCHAR(128) NOT NULL,
    column_name VARCHAR(128) NOT NULL,
    current_type VARCHAR(128),
    current_length INTEGER,
    current_nullable BOOLEAN,
    requested_type VARCHAR(128),
    requested_length INTEGER,
    metadata_digest VARCHAR(64) NOT NULL,
    impact_summary VARCHAR(1000) NOT NULL,
    confirmation_digest VARCHAR(64) NOT NULL,
    plan_status VARCHAR(24) NOT NULL DEFAULT 'PREVIEWED',
    failure_code VARCHAR(64),
    created_by BIGINT NOT NULL,
    applied_by BIGINT,
    create_time TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    applied_at TIMESTAMP WITHOUT TIME ZONE,
    CONSTRAINT uk_datasource_schema_repair_plan_ref UNIQUE (plan_ref),
    CONSTRAINT ck_datasource_schema_repair_operation CHECK (
        operation IN ('ADD_NULLABLE_COLUMN', 'WIDEN_VARCHAR', 'DROP_NOT_NULL')
    ),
    CONSTRAINT ck_datasource_schema_repair_status CHECK (
        plan_status IN ('PREVIEWED', 'APPLIED', 'FAILED', 'STALE')
    )
);

CREATE INDEX IF NOT EXISTS idx_datasource_schema_repair_scope
    ON datasource_schema_repair_plan (tenant_id, project_id, datasource_id, id DESC);

CREATE INDEX IF NOT EXISTS idx_datasource_schema_repair_status
    ON datasource_schema_repair_plan (plan_status, update_time DESC);

COMMENT ON TABLE datasource_schema_repair_plan IS
    'Digest-bound datasource schema repair previews and apply audit. Raw DDL is intentionally not persisted.';
