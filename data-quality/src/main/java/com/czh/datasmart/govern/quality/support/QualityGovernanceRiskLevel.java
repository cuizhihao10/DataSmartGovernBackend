package com.czh.datasmart.govern.quality.support;

/**
 * 数据质量治理总览风险等级。
 *
 * <p>这个枚举服务的是“治理大盘”而不是单次质量报告。单次报告只判断某条规则本次检测
 * PASSED/FAILED，而治理总览需要综合规则覆盖、近期检测频率、失败报告、执行器失败、
 * 异常样本积压等多种信号，给运营人员一个可排序、可解释的风险入口。</p>
 *
 * <p>为什么不直接复用 HIGH/MEDIUM/LOW：规则严重级别描述的是“某条规则失败后的业务影响”，
 * 而本枚举描述的是“当前治理范围整体是否健康”。两者语义不同，拆开后更利于后续告警、
 * SLA 和质量评分体系独立演进。</p>
 */
public enum QualityGovernanceRiskLevel {

    /**
     * 当前请求在 PROJECT 数据范围下没有任何可见项目。
     *
     * <p>这不是业务健康，也不是业务失败，而是权限收口后的空结果。前端或 Agent 不应据此
     * 判断“质量良好”，而应提示用户切换项目或申请权限。</p>
     */
    NO_VISIBLE_PROJECT,

    /**
     * 当前范围尚未建立有效治理事实。
     *
     * <p>常见原因包括没有规则、没有启用规则、近期没有检测报告。它代表产品闭环未启动，
     * 比“检测失败”更早一层，需要先补齐规则覆盖和执行计划。</p>
     */
    NOT_STARTED,

    /**
     * 治理状态健康。
     *
     * <p>规则覆盖、近期检测和报告结果都比较稳定，仍建议结合业务 SLA 和趋势继续观察。</p>
     */
    HEALTHY,

    /**
     * 需要关注。
     *
     * <p>存在少量失败、异常样本、规则草稿积压或执行链路提示，但还没有达到高风险程度。</p>
     */
    WATCH,

    /**
     * 存在明显风险。
     *
     * <p>通常表示失败报告比例、异常积压或执行失败已经影响治理可信度，需要进入异常工作台、
     * 规则复核或执行器排障流程。</p>
     */
    RISK,

    /**
     * 严重风险。
     *
     * <p>治理评分很低或关键事实缺失严重，适合触发告警、人工复核、清洗任务或暂停依赖该数据的下游流程。</p>
     */
    CRITICAL
}
